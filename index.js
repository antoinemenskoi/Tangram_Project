/*index */

const dropdownchild = document.querySelector('.dropdown-child');
dropdownchild.addEventListener('click', () => {
	dropdownchild.classList.toggle('active');
});

function index() {
	var theme = document.getElementById("change-style");
	if (theme.getAttribute("href") != "css/index.css") {
		theme.setAttribute("href", "css/index.css");
	} else {
		theme.setAttribute("href", "css/index.css");
	}
}

